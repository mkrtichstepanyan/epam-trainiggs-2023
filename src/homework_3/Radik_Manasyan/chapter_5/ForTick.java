package homework_3.Radik_Manasyan.chapter_5;

public class ForTick {
    public static void main(String[] args) {
        int n;
        for (n = 10; n > 0; n--) {
            System.out.println("Такт " + n);
        }
        System.out.println("class ForTick version number 2");
        for (int n2 = 10; n2 > 0; n2--) {
            System.out.println("Такт " + n2);

        }
    }
}
