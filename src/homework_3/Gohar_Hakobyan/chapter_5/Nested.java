package homework_3.Gohar_Hakobyan.chapter_5;

public class Nested {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            for (int j = i; j < 10; j++) {
                System.out.print(".");

            }
            System.out.println();
        }

    }
}

