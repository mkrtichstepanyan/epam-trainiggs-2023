package homework_3.Alina_Vardanovna.chapter5;

public class SampleSwitch {

    public static void main(String[] args) {

        for (int i = 0; i < 6; i++) {
            switch (i) {
                case 0:
                    System.out.println("i-ն 0 է:");
                    break;
                case 1:
                    System.out.println("i-ն 1 է:");
                    break;
                case 2:
                    System.out.println("i-ն 2 է:");
                    break;
                case 3:
                    System.out.println("i-ն 3 է:");
                    break;
                default:
                    System.out.println("i-ն 3-ից մեծ է:");
            }
        }

    }

}
