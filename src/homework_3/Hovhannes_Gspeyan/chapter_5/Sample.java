package homework_3.Hovhannes_Gspeyan.chapter_5;

/**
 * for loop demonstration
 */
public class Sample {
    public static void main(String[] args) {
        int a, b;
        
        b = 4;
        for (a = 1; a < b; a++) {
            System.out.println("a = " + a);
            System.out.println("b = " + b);
            b--;
        }
    }
}
