package Roza_Petrosyan;

// Compute the area of a circle.
public class Area {
    public static void main(String[] args){
        double pi, r, a;
        r = 1.8; // radius of circle
        pi = 3.1416; // pi, approximately;
        a = pi * r * r; // compute area;

        System.out.println("Area1 of circle is " + a);
    }
}