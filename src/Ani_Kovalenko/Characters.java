package Ani_Kovalenko;

public class Characters {
    public static void main(String[] args) {
        char ch1, ch2;
        ch1 = 88; //identified to "X"
        ch2 = 'Y';

        System.out.println("ch1 and ch2: " + ch1 + " " + ch2);

        //increment
        ch1 = 'X';
        System.out.println ("ch1: " + ch1);
        ch1++;
        System.out.println ("ch1++ : " + ch1);

    }
}
