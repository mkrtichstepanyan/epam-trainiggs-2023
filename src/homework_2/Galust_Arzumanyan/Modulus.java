// This method is used to demonstrate % operator for int and double
class Modulus {
    public static void main(String[] args) {
        int a = 77;
        double db = 77.7;
        System.out.println("a mod 5 is " + a % 5);
        System.out.println("db mot 5 is " + db % 5);
    }
}
