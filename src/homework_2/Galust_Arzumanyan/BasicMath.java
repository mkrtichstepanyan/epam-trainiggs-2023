package homework_2.Galust_Arzumanyan;

// This method is used to demonstrate simple math operators
class BasicMath {
    public static void main(String[] args) {
        int a, b, c, d, e;
        a = 7;
        b = a + 1;
        c = b - 2;
        d = c * 3;
        e = d / 4;
        System.out.println("The values with int type");
        System.out.println("a is: " + a);
        System.out.println("b is: " + b);
        System.out.println("c is: " + c);
        System.out.println("d is: " + d);
        System.out.println("e is: " + e);
        double da, db, dc, dd, de;
        da = 7;
        db = da + 1;
        dc = db - 2;
        dd = dc * 3;
        de = dd / 4;
        System.out.println("The values with double type");
        System.out.println("da is: " + da);
        System.out.println("db is: " + db);
        System.out.println("dc is: " + dc);
        System.out.println("dd is: " + dd);
        System.out.println("de is: " + de);
    }
}
