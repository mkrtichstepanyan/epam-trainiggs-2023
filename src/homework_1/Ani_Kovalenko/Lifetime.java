package homework_1.Ani_Kovalenko;

public class Lifetime {
    public static void main(String[] args) {
        int x;
        for (x = 0; x < 3; x++) {
            int y = -1;
            System.out.println("y equals to " + y);
            y = 100;
            System.out.println("Now y equals to " + y);
        }
    }
}
