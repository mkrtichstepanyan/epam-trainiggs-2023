package homework_1.Gohar_Hakobyan;

public class CharDemo2 {
    public static void main(String[] args) {
        char ch1 = 'X';
        char ch2 = 'x';
        System.out.println("ch1 contains " + ch1);
        ch1++;// Increase the value of ch1 by one
        System.out.println("ch1 contains " + ch1++);
        ch2--;
        System.out.println("ch2 contains " + ch2--);
    }

}
